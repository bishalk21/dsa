/** Sliding Window
 * - a technique used to solve problems that involve finding a contiguous subarray or substring
 *   that satisfies certain conditions.
 * - used when we need to handle the input data in a specific window size or
 *   when we need to find a specific pattern in a contiguous manner.
 * - It involves maintaining a window of elements and sliding it across the data structure to find the desired result.
 *
 * Data Structures involved: Arrays, Strings, Linked Lists, HashTables, etc.
 *
 * The basic idea is to use two pointers (or indices) to represent the start and end of the window.
 * The window can be expanded or contracted based on the conditions of the problem.
 * This technique is often used to optimize time complexity from O(n^2) to O(n) in certain problems.
 *
 * Example problems that can be solved using the sliding window technique include:
 * - Finding the maximum sum of a subarray of size k.
 * - Finding the longest substring without repeating characters.
 */
