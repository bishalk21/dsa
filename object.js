// Object in JavaScript
// * An object is a collection of key-value pairs, where keys are strings (or Symbols) and values can be any data type.
// * Objects can be created using object literals, constructors, or the Object.create() method.
// * Objects can be nested, meaning an object can contain other objects or arrays as values.
// * Objects can be manipulated using various methods, such as adding, updating, or deleting properties.
// * Objects can also have methods, which are functions associated with the object.
