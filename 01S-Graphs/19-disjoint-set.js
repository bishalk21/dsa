/** Disjoint Set (Union-Find) Data Structure
 * - also known as a union-find data structure,
 * - keeps track of a collection of non-overlapping sets.
 * - It provides efficient operations for merging sets and
 *   finding the representative (or "parent") of a set.
 *
 * - The main operations of a disjoint set are:
 *  1. **Find**: Determine which set a particular element belongs to.
 *  2. **Union**: Merge two sets into one.
 *
 * - This is particularly useful in algorithms that involve grouping elements,
 *   such as Kruskal's algorithm for finding the minimum spanning tree of a graph.
 * - The Find and Union operations are specially used in detecting cycles in a graph,
 *   and in network connectivity problems.
 */
